import { ComponentFixture, TestBed } from '@angular/core/testing';
import { Observable, of } from 'rxjs';
import { HttpHeaders, HttpResponse } from '@angular/common/http';
import { ActivatedRoute, Data } from '@angular/router';

import { PlantyComparePortalTestModule } from '../../../../test.module';
import { PurchasingPowerComponent } from 'app/entities/PlantyComparePurchasingPower/purchasing-power/purchasing-power.component';
import { PurchasingPowerService } from 'app/entities/PlantyComparePurchasingPower/purchasing-power/purchasing-power.service';
import { PurchasingPower } from 'app/shared/model/PlantyComparePurchasingPower/purchasing-power.model';

describe('Component Tests', () => {
  describe('PurchasingPower Management Component', () => {
    let comp: PurchasingPowerComponent;
    let fixture: ComponentFixture<PurchasingPowerComponent>;
    let service: PurchasingPowerService;

    beforeEach(() => {
      TestBed.configureTestingModule({
        imports: [PlantyComparePortalTestModule],
        declarations: [PurchasingPowerComponent],
        providers: [
          {
            provide: ActivatedRoute,
            useValue: {
              data: {
                subscribe: (fn: (value: Data) => void) =>
                  fn({
                    pagingParams: {
                      predicate: 'id',
                      reverse: false,
                      page: 0
                    }
                  })
              }
            }
          }
        ]
      })
        .overrideTemplate(PurchasingPowerComponent, '')
        .compileComponents();

      fixture = TestBed.createComponent(PurchasingPowerComponent);
      comp = fixture.componentInstance;
      service = fixture.debugElement.injector.get(PurchasingPowerService);
    });

    it('Should call load all on init', () => {
      // GIVEN
      const headers = new HttpHeaders().append('link', 'link;link');
      spyOn(service, 'query').and.returnValue(
        of(
          new HttpResponse({
            body: [new PurchasingPower('123')],
            headers
          })
        )
      );

      // WHEN
      comp.ngOnInit();

      // THEN
      expect(service.query).toHaveBeenCalled();
      expect(comp.purchasingPowers[0]).toEqual(jasmine.objectContaining({ id: '123' }));
    });

    it('should load a page', () => {
      // GIVEN
      const headers = new HttpHeaders().append('link', 'link;link');
      spyOn(service, 'query').and.returnValue(
        of(
          new HttpResponse({
            body: [new PurchasingPower('123')],
            headers
          })
        )
      );

      // WHEN
      comp.loadPage(1);

      // THEN
      expect(service.query).toHaveBeenCalled();
      expect(comp.purchasingPowers[0]).toEqual(jasmine.objectContaining({ id: '123' }));
    });

    it('should re-initialize the page', () => {
      // GIVEN
      const headers = new HttpHeaders().append('link', 'link;link');
      spyOn(service, 'query').and.returnValue(
        of(
          new HttpResponse({
            body: [new PurchasingPower('123')],
            headers
          })
        )
      );

      // WHEN
      comp.loadPage(1);
      comp.reset();

      // THEN
      expect(comp.page).toEqual(0);
      expect(service.query).toHaveBeenCalledTimes(2);
      expect(comp.purchasingPowers[0]).toEqual(jasmine.objectContaining({ id: '123' }));
    });
    it('should calculate the sort attribute for an id', () => {
      // WHEN
      const result = comp.sort();

      // THEN
      expect(result).toEqual(['id,asc']);
    });

    it('should calculate the sort attribute for a non-id attribute', () => {
      // GIVEN
      comp.predicate = 'name';

      // WHEN
      const result = comp.sort();

      // THEN
      expect(result).toEqual(['name,asc', 'id']);
    });
  });
});
